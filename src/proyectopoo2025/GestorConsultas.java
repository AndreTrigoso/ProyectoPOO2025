    private void cargarDatosATabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);

        for (int i = 0; i < gestorConsulta.getCantidad(); i++) {
            Consulta c = gestorConsulta.getConsultas(i);

            Paciente p = c.getPaciente();

            String paciente = p.getNombre() + " " + p.getApellido();
            String motivo = c.getMotivo();
            double precio = c.getPrecio();
            String estado = c.getEstado();

            modelo.addRow(new Object[]{
                    paciente, motivo, precio, estado
            });
        }
    }

